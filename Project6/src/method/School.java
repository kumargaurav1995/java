package method;

public class School {
	double id;
	String name;
	Double percent;
	public double getId() {
		return id;
	}
	public void setId(double i) {
		this.id = i;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Double getPercent() {
		return percent;
	}
	public void setPercent(Double percent) {
		this.percent = percent;
	}

}
